package dao;

public class DocADTO {
	private String docNo;
	private String docName;
	private String deptNo;
	private String docCareer;
	private String docInstitute;
	private String cureName;
	private String favor;
	
	public String getDocNo() {
		return docNo;
	}
	public void setDocNo(String docNo) {
		this.docNo = docNo;
	}
	public String getDocName() {
		return docName;
	}
	public void setDocName(String docName) {
		this.docName = docName;
	}
	public String getDeptNo() {
		return deptNo;
	}
	public void setDeptNo(String deptNo) {
		this.deptNo = deptNo;
	}
	public String getDocCareer() {
		return docCareer;
	}
	public void setDocCareer(String docCareer) {
		this.docCareer = docCareer;
	}
	public String getDocInstitute() {
		return docInstitute;
	}
	public void setDocInstitute(String docInstitute) {
		this.docInstitute = docInstitute;
	}
	public String getCureName() {
		return cureName;
	}
	public void setCureName(String cureName) {
		this.cureName = cureName;
	}
	public String getFavor() {
		return favor;
	}
	public void setFavor(String favor) {
		this.favor = favor;
	}
	
	
		
}
